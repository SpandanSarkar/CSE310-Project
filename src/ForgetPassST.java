    private void jButton_ConfirmPassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ConfirmPassActionPerformed
        PreparedStatement ps;

        String uname = jTextUname.getText();
        String pass = jTextPass.getText();
        String query;
        query = "UPDATE `student profile` SET `pass`=? WHERE `uname`=?";
        try {
            ps = MyConnection.getConnection().prepareStatement(query);
            ps.setString(1, pass);
            ps.setString(2, uname);
            ps.executeUpdate();
            PassSuccess mf = new PassSuccess();
            mf.setVisible(true);
            mf.pack();
            mf.setLocationRelativeTo(null);
            mf.setExtendedState(JFrame.MAXIMIZED_BOTH);
            this.dispose();

        } catch (SQLException ex) {
            Logger.getLogger(ForgetPassST.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton_ConfirmPassActionPerformed

